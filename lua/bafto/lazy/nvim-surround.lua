return {
	'kylechui/nvim-surround',

	tag = "v2.1.4",

	config = function()
		require("nvim-surround").setup {
			surrounds = {
				["("] = {
					add = { "(", ")" },
					delete = "^(.)().-(.)()$",
				},
				[")"] = {
					add = { "( ", " )" },
					delete = "^(. ?)().-( ?.)()$",
				},
				["{"] = {
					add = { "{", "}" },
					delete = "^(.)().-(.)()$",
				},
				["}"] = {
					add = { "{ ", " }" },
					delete = "^(. ?)().-( ?.)()$",
				},
				["<"] = {
					add = { "<", ">" },
					delete = "^(.)().-(.)()$",
				},
				[">"] = {
					add = { "< ", " >" },
					delete = "^(. ?)().-( ?.)()$",
				},
				["["] = {
					add = { "[", "]" },
					delete = "^(.)().-(.)()$",
				},
				["]"] = {
					add = { "[ ", " ]" },
					delete = "^(. ?)().-( ?.)()$",
				},
				["'"] = {
					add = { '"', '"' },
					delete = "^(.)().-(.)()$",
				},
				['"'] = {
					add = { "'", "'" },
					delete = "^(.)().-(.)()$",
				},
				["`"] = {
					add = { "`", "`" },
					delete = "^(.)().-(.)()$",
				},
			},
		}
	end,
}
