require("nvim-surround").setup {
	surrounds = {
		["("] = {
			add = { "(", ")" },
			delete = "^(.)().-(.)()$",
		},
		[")"] = {
			add = { "( ", " )" },
			delete = "^(. ?)().-( ?.)()$",
		},
		["{"] = {
			add = { "{", "}" },
			delete = "^(.)().-(.)()$",
		},
		["}"] = {
			add = { "{ ", " }" },
			delete = "^(. ?)().-( ?.)()$",
		},
		["<"] = {
			add = { "<", ">" },
			delete = "^(.)().-(.)()$",
		},
		[">"] = {
			add = { "< ", " >" },
			delete = "^(. ?)().-( ?.)()$",
		},
		["["] = {
			add = { "[", "]" },
			delete = "^(.)().-(.)()$",
		},
		["]"] = {
			add = { "[ ", " ]" },
			delete = "^(. ?)().-( ?.)()$",
		},
		["'"] = {
			add = { '"', '"' },
			delete = "^(.)().-(.)()$",
		},
		['"'] = {
			add = { "'", "'" },
			delete = "^(.)().-(.)()$",
		},
		["`"] = {
			add = { "`", "`" },
			delete = "^(.)().-(.)()$",
		},
	},
}
